package model.rec;

public class BoothRevVo {
	int boothRevCount, boothRevNo, boothRevOrder;
	String boothRevTime, memberName;
	public int getBoothRevCount() {
		return boothRevCount;
	}
	public void setBoothRevCount(int boothRevCount) {
		this.boothRevCount = boothRevCount;
	}
	public int getBoothRevNo() {
		return boothRevNo;
	}
	public void setBoothRevNo(int boothRevNo) {
		this.boothRevNo = boothRevNo;
	}
	public int getBoothRevOrder() {
		return boothRevOrder;
	}
	public void setBoothRevOrder(int boothRevOrder) {
		this.boothRevOrder = boothRevOrder;
	}
	public String getBoothRevTime() {
		return boothRevTime;
	}
	public void setBoothRevTime(String boothRevTime) {
		this.boothRevTime = boothRevTime;
	}
	public String getMemberName() {
		return memberName;
	}
	public void setMemberName(String memberName) {
		this.memberName = memberName;
	}
	

}
